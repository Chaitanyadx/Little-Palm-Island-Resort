     Extensive renovations followed to create forty-one luxurious rooms and suites, a sumptuous Spa and beautiful facilities for hotel guests.
      Two of the private residences were also purchased to create spectacular villas for small groups of friends or family.
     Whilst a plant nursery was crafted to cultivate fresh produce and herbs for the restaurants.</p><br><br>

