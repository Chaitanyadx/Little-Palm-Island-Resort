    a practice Johnny had learnt from previous projects, giving him the nickname “Johnny Coconuts”.
     And so Palm Island evolved and by leasing a few select building plots for private homes the funds were raised to
      construct a ten-bedroom hotel, The Palm Island Beach Club, which they ran for the next thirty years.</p><br><br>

    <p class="main_dining_room" align="center">
